    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        String idcurso = request.getParameter("idcurso");
        boolean resultado = new MantCursoDAO().EliminarCurso(idcurso);
        String respuesta = "Error al eliminar el curso", colormsg = "danger";
        if(resultado){
              respuesta = "Se elimin√≥ el curso correctamente";
              colormsg = "primary";
        }
        List<Curso> lstcursos = new MantCursoDAO().ListarCursos();
        request.setAttribute("lstcursos", lstcursos);
        request.setAttribute("msgeliminar", respuesta);
        request.setAttribute("colormsgeliminar", colormsg);
        RequestDispatcher dispatcher = request.getRequestDispatcher("/ListaCursos.jsp");
        dispatcher.forward(request, response);
        
    }

